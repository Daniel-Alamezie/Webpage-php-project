    <h1>SALVAGED SHARDS INTO SCULPTURAL ARCHAEOLOGICAL MOSAICS</h1>
    <p>24 OCTOBER 2020
      10 NOVEMBER 2020
      to
      31 MARCH 2021
      <br>
      Philip Hardaker
      shards of ceramics
      Philip Hardaker has been producing art for over 40 years. Working with over 50,000 people in education, community and the environment as well as completing many public and private commissions.<br>
      <br>
      Born in Harrogate North Yorkshire, he graduated from North Staffordshire University Fine Art Sculpture department in 1977. He then went on to the RCA (Royal College of Art) in London where he gained an MA in fine art ceramics. His tutors included the sculptor Sir Eduardo Paolozzi, the painter Sir Peter Blake and Professor David Queensbury. These great tutors had a strong influence on the artists ambitions and intentions. <br>
      <br>
      His private commissions include the late and great Kirsty MacColl, Billy Bragg, The Pogues, Steve Lillywhite, and the McCartney family. Philip has also completed commissions for HRH Prince Charles, The Houses of Parliament, many councils and two commissions at Keele University. <br>
      <br>
      The work on show documents the times we all live in, connecting with environmental issues, political influences, climate change, anti-war sentiments, Covid-19… all created with serious intentions and a wry sense of humour and irony<br>
      <br>
