    <h1>THREE COUNTIES OPEN ART CUBES</h1>
    <br>
    <p>24 OCTOBER 2020
      to
      27 MARCH 2021<br>
      This illuminated exhibition will see 4 huge cubes take centre stage as they tour from Stoke-on-Trent to Newcastle-under-Lyme and complete their journey at Keele<br>

      Three Counties Cube ogo
      Following on from the success of the first virtual Three Counties Open Art exhibition this summer, we now have an exciting opportunity to showcase some of the talented work from the exhibition by artists from Cheshire, Shropshire & Staffordshire.<br>
      <br>

      Appetite and ArtsKeele are really excited to be bringing The Three Counties Cubes to life over the coming months. This illuminated exhibition is perfect for the darker days and will see 4 huge cubes take centre stage as they tour from Stoke-on-Trent to Newcastle-under-Lyme and complete their journey at Keele University in the New Year. The cubes will feature some of the works from the summer show.<br>
      <br>


      Run by ArtsKeele the Three Counties Open Art Exhibition has been taking place since the early 2000s and since 2017 has taken place in the historic Burslem School of Art. It is ‘open’ to anyone from Staffordshire, Cheshire and Shropshire to submit a piece of artwork. In spite of the pandemic, The Three Counties team and partners were committed to making it happen in 2020 and the project this year took place virtually with 145 works appearing in the online exhibition in August.<br>
      <br>

      Appetite got involved in the exhibition this year and offered a brand new prize – the Appetite Audience Choice Award. This saw Appetite’s Supper Club come up with a shortlist of 20 works which were then put to the public to vote for their favourite. The winner, runners up and shortlisted pieces can all be viewed as part of the exhibition.<br>
      <br>
      The Cubes will also highlight winners from different categories of the exhibition and some COVID inspired works.
