    <h1>STRONG, BRITISH AND BLACK.</h1><br>
    <p>04 DECEMBER 2020<br>
      26 SEPTEMBER 2020<br>
      Strong, British and Black is a Kwanzaa Collective UK* exhibition which showcases the art of black and brown women living and working in Staffordshire. Building on the themes of the Cultural Affairs I AM exhibition (2019) this exhibition centres the voices of black and brown women to explore what it means to be Strong, British and Black. Using film, photography, painting, poetry, sculpture and digital art, the artists explore the themes of the exhibition individually, while looking at how they interact. What does it mean to a strong woman? What does it mean to be Strong and Black? What does it mean to be Black British in a country that often considers you Black in Britain?<br>



